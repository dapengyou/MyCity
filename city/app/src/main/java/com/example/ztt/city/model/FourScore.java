public class FourScore {
    private String totaleScore;//总分
    private String listenScore;//听力
    private String readScore;//阅读
    private String writeScore;//写作与翻译


    public String getTotaleScore() {
        return totaleScore;
    }

    public void setTotaleScore(String totaleScore) {
        this.totaleScore = totaleScore;
    }

    public String getListenScore() {
        return listenScore;
    }

    public void setListenScore(String listenScore) {
        this.listenScore = listenScore;
    }

    public String getReadScore() {
        return readScore;
    }

    public void setReadScore(String readScore) {
        this.readScore = readScore;
    }

    public String getWriteScore() {
        return writeScore;
    }

    public void setWriteScore(String writeScore) {
        this.writeScore = writeScore;
    }
}
